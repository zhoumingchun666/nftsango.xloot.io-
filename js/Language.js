    var lan = getCookie('lan'); //语言版本
    //选取语言文字
    switch (lan) {
        case 'cn':
            var t = cn[m];
            break;
        case 'hk':
            var t = hk[m];
            break;
        case 'en':
            var t = en[m];
            break;
        default:
            var t = cn[m];
            // var t = hk[m];
    }
    //如果所选语言的json中没有此内容就选取其他语言显示
    if (t == undefined) t = cn[m];
