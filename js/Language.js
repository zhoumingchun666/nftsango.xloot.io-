$(function() {
    $('[set-lan]').each(function() {
        var me = $(this);
        var a = me.attr('set-lan').split(':');
        var p = a[0]; //文字放置位置
        var m = a[1] || a[0]; //文字的标识
        //用户选择语言后保存在cookie中，这里读取cookie中的语言版本
        var lan = getCookie('lan');
        // console.log(m);
        // console.log("lan:",m);
        //选取语言文字
        switch (lan) {
            case 'cn':
                var t = cn[m]; //这里cn[m]中的cn是上面定义的json字符串的变量名，m是json中的键，用此方式读取到json中的值
                break;
            case 'en':
                var t = en[m];
                break;
            case 'hk':
                var t = hk[m];
                break;
            default:
                var t = cn[m];
                // var t = hk[m];
        }
        //如果所选语言的json中没有此内容就选取其他语言显示
        if (t == undefined) t = cn[m];
        if (t == undefined) t = hk[m];
        if (t == undefined) t = en[m];
        if (t == undefined) return true; //如果还是没有就跳出
        //文字放置位置有（html,val等，可以自己添加）
        switch (p) {
            case 'html':
                me.html(t);
                break;
            case 'alt':
                me.attr("alt", t);
                break;
            case 'val':
            case 'value':
                me.val(t);
                break;
            case 'tip':
                me.attr("placeholder", t);
                break;
            default:
                me.html(t);
        }
    });
})

